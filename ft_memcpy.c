#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = 0;
	while (i < n || d[i] != '\0' || s[i] != '\0')
	{
		d[i] = s[i];
	}
	dest = d;
	src = s;
}
